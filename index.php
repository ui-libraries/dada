					<p>The Digital Dada Library provides links to scanned images of original Dada-era publication in the International Dada Archive.  These books, pamphlets, and periodicals are housed in the Special Collections Department of The University of Iowa Libraries.  </p>
					<p>Each original document has been scanned in its entirety. These are page-image files only, not searchable full-text files. </p>

					<p>The Digital Library is divided into two sections. The first section includes some of the major periodicals of the Dada movement from Zurich, Berlin, Paris, and elsewhere. In many cases, these are not complete runs of the periodicals. While the International Dada Archive does own complete reprints of most of these periodicals, we generally only include original issues housed in Special Collections. In a few cases, for the sake of completeness, we have included reprints of issues for which we do not own originals; in those cases, we have omitted any copyrighted materials such as new prefaces and critical annotations. The second section includes books by some of the participants in the Dada movement, as well as some of the more ephemeral Dada-era publications, such as exhibition catalogs and broadsides.</p>

