				<h2>Online <br><strong>Bibliography</strong> </h2>
				<div class="entry">
					<p>The International On-line Bibliography of Dada is the on-line catalog of the International Dada Archive at the University of Iowa Libraries. The catalog includes materials in all formats that have been cataloged for the collection, including books, essays in books, periodical articles, manuscripts, sound and videorecordings, and other media related to the Dada movement and to the individual dadaists. These materials are located throughout the University of Iowa Libraries, but are primarily housed in the Main Library and the Art Library. Most of the manuscript holdings are on microfilms that were made in various public and private collections in Europe and North America in the early 1980s, with funding from the National Endowment for the Humanities and the Jerome Foundation.</p>
					<p>The International Dada Archive has no staff to process interlibrary loan requests. Once you have identified a book or article of interest in the database, please use your library's interlibrary loan services to obtain the item if your library does not own a copy. Your library should treat the International Dada Archive as a "lender of last resort." For interlibrary loan purposes, the International Dada Archive should be treated as a special collection within the University of Iowa Libraries. Unpublished manuscript material cannot normally be loaned.</p>
